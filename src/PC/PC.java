package PC;

/**
 *
 * @author cwr
 */
public class PC {
    private String username , pc;
    private int biaya , waktu , total;
    
    public String getUsername() {
        return username;
    }
    
    public void setUsername(String username) {
        this.username = username;
    }
    
    public String getPc() {
        return pc;
    }
    
    public void setPc(String pc) {
        this.pc = pc;
    }
    
    public int getBiaya() {
        return biaya;
    }
    
    public void setBiaya(int biaya) {
        this.biaya = biaya;
    }
    
    public int getWaktu() {
        return waktu;
    }
    
    public void setWaktu(int waktu) {
        this.waktu = waktu;
    }
    
    public int getTotal() {
        return total;
    }
    
    public void setTotal(int total) {
        this.total = total;
    }
    
}
