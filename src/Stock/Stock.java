public class Stock {
    private String barang;
    private int jumlah , harga;
    
    public String getBarang() {
        return barang;
    }
    
    public void setBarang(String barang) {
        this.barang = barang;
    }
    
    public int getJumlah() {
        return jumlah;
    }
    
    public void setJumlah(int jumlah) {
        this.jumlah = jumlah;
    }
    
    public int getHarga() {
        return harga;
    }
    
    public void setHarga(int harga) {
        this.harga = harga;
    }
    
}
